

# write python program to find remainder when a numer is divided by X;


# This program calculates the remainder when a number is divided by X

# Print the program description
print("Program to find the remainder when a number is divided by X")

# Take the number as input from the user and convert it to an integer
number = int(input("Enter the number: "))

# Take the divisor (X) as input from the user and convert it to an integer
divisor = int(input("Enter the divisor (X): "))

# Calculate the remainder
remainder = number % divisor

# Print the result
print("The remainder when", number, "is divided by", divisor, "is:", remainder)

# Print the end of the program message
print("========= END =============")


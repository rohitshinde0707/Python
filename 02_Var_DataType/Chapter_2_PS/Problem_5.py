# Q| Write a program to find an avg of two number enterd by the user



# This program calculates the average of two numbers entered by the user

# Take the first number as input from the user and convert it to an integer
num_1 = int(input("Enter First Number::"))

# Take the second number as input from the user and convert it to an integer
num_2 = int(input("Enter Second Number::"))

# Calculate the sum of the two numbers
sum = num_1 + num_2

# Calculate the average of the two numbers
avg = sum / 2

# Print the average of the two numbers
print("The average of the two numbers is:", avg)
